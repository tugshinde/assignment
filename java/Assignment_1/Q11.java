public class Q11 {
    public static void main (String[] arg){
        System.out.println("Numbers from 1 to 50 by using for loop");
        for(int i=1;i<=50;i++){
            System.out.print(i+" ");
        }
        System.out.println("\nNumbers from 1 to 50 by using while loop");
        int i =1;
        while (i<=50) {
            System.out.print(i+" ");
            i++;
            
        }
    }
    
}
